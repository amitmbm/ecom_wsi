package com.wsi.dto;


public class LinksDTO {
	private String rel;
	private String href;

	public LinksDTO() {}

	public String getRel() {
		return rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
}
